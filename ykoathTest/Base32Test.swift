import XCTest
@testable import ykoath

final class Base32Test: XCTestCase {
    func testConversion() {
        let encodedString = "KRUGKIDROVUWG2ZAMJZG653OEBTG66BANJ2W24DTEBXXMZLSEB2GQZJANRQXU6JAMRXWOLQ="
        let expectedData = Data([
            0x54, 0x68, 0x65, 0x20, 0x71, 0x75,
            0x69, 0x63, 0x6b, 0x20, 0x62, 0x72,
            0x6f, 0x77, 0x6e, 0x20, 0x66, 0x6f,
            0x78, 0x20, 0x6a, 0x75, 0x6d, 0x70,
            0x73, 0x20, 0x6f, 0x76, 0x65, 0x72,
            0x20, 0x74, 0x68, 0x65, 0x20, 0x6c,
            0x61, 0x7a, 0x79, 0x20, 0x64, 0x6f,
            0x67, 0x2e
        ])

        XCTAssertEqual(expectedData, Data(base32Encoded: encodedString))
    }
}
